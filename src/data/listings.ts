let counter = 0;

function buildListing(spec: Spec): Listing {
  counter++;
  const id = `lst-${String(counter).padStart(4, "0")}`;
  const titleParts = [spec.make, spec.model, spec.variant].filter(Boolean);
  const title = `${titleParts.join(" ")} · ${spec.year}.`;
  const slug = `${slugify(`${spec.make}-${spec.model}-${spec.variant ?? ""}-${spec.year}-${spec.city}`)}-${id}`;
  const createdAt = new Date(Date.now() - spec.daysAgo * 24 * 60 * 60 * 1000).toISOString();
  return {
    id,
    slug,
    title,
    make: spec.make,
    model: spec.model,
    variant: spec.variant,
    year: spec.year,
    priceEur: spec.priceEur,
    km: spec.km,
    fuel: spec.fuel,
    transmission: spec.transmission,
    bodyType: spec.bodyType,
    drive: spec.drive,
    color: spec.color,
    condition: spec.condition ?? (spec.km < 5000 ? "Novo" : "Rabljeno"),
    engineCc: spec.engineCc,
    powerKw: spec.powerKw,
    doors: spec.doors ?? (spec.bodyType === "Hatchback" || spec.bodyType === "Coupe" ? 5 : 5),
    seats: spec.seats ?? 5,
    city: spec.city,
    county: spec.county,
    description: spec.description,
    features: spec.features,
    images: spec.photos,
    sellerName: spec.sellerName,
    sellerType: spec.sellerType,
    sellerPhone: spec.sellerPhone,
    views: spec.views ?? Math.floor(Math.random() * 850) + 80,
    featured: spec.featured ?? false,
    createdAt,
    registrationUntil: spec.registrationUntil,
    firstRegistered: `${String(Math.floor(Math.random() * 12) + 1).padStart(2, "0")}/${spec.year}`,
  };
}

export const LISTINGS: Listing[] = SPECS.map(buildListing);

export function getListing(slug: string): Listing | undefined {
  return LISTINGS.find((l) => l.slug === slug);
}

export function getListingsByMake(makeSlug: string): Listing[] {
  return LISTINGS.filter(
    (l) => slugify(l.make) === makeSlug
  );
}

export function getFeaturedListings(limit = 6): Listing[] {
  return LISTINGS.filter((l) => l.featured).slice(0, limit);
}

export function getRelatedListings(listing: Listing, limit = 4): Listing[] {
  return LISTINGS.filter(
    (l) => l.id !== listing.id && (l.make === listing.make || l.bodyType === listing.bodyType)
  ).slice(0, limit);
}
